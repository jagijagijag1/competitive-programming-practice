package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	a, b, c, x, y := nextInt(), nextInt(), nextInt(), nextInt(), nextInt()

	r1 := a*x + b*y
	r2 := max(x, y) * 2 * c
	r3 := min(x, y) * 2 * c
	if x > y {
		r3 += (x - y) * a
	} else {
		r3 += (y - x) * b
	}

	fmt.Println(min(r1, min(r2, r3)))
}

func min(a, b int) int {
	if a > b {
		return b
	}
	return a
}

func max(a, b int) int {
	if a < b {
		return b
	}
	return a
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

