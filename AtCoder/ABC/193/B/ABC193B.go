package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	n, res := nextInt(), 1000000001
	for i := 0; i < n; i++ {
		a, p, x := nextInt(), nextInt(), nextInt()
		if a < x {
			res = min(res, p)
		}
	}
	if res == 1000000001 {
		fmt.Println(-1)
	} else {
		fmt.Println(res)
	}
}

func min(a, b int) int {
	if a > b {
		return b
	}
	return a
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

