package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	x, k, d := abs(nextInt()), nextInt(), nextInt()

	req := x / d
	if k <= req {
		x -= k * d
	} else {
		x -= req * d
		k -= req
		if k%2 == 1 {
			x -= d
		}
	}

	fmt.Println(abs(x))
}

func abs(a int) int {
	if a < 0 {
		return -a
	}
	return a
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

