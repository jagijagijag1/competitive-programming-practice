package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	N, K, m := nextInt(), nextInt(), map[int]int{}
	for i := 0; i < N; i++ {
		a, b := nextInt(), nextInt()
		m[a] += b
	}

	cnt := 0
	for i := 1; i <= 100000; i++ {
		cnt += m[i]
		if K <= cnt {
			fmt.Println(i)
			return
		}
	}
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

