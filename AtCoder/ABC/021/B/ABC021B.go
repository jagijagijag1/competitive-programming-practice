package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	_, a, b, k, p := nextInt(), nextInt(), nextInt(), nextInt(), map[int]bool{}
	for i := 0; i < k; i++ {
		pp := nextInt()
		if pp == a || pp == b || p[pp] {
			fmt.Println("NO")
			return
		}
		p[pp] = true
	}
	fmt.Println("YES")
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

