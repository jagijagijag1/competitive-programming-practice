package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	n, res := nextInt(), 0

	for i := 1; i <= n; i += 2 {
		d := divisor(i)
		if len(d) == 8 {
			res++
		}
	}
	fmt.Println(res)
}

func divisor(n int) (res []int) {
	for i := 1; i*i < n; i++ {
		if n%i == 0 {
			res = append(res, i)
			if i*i != n {
				res = append(res, n/i)
			}
		}
	}
	return
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

