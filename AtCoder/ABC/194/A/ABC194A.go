package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	a, b := nextInt(), nextInt()
	if 15 <= a+b && 8 <= b {
		fmt.Println(1)
	} else if 10 <= a+b && 3 <= b {
		fmt.Println(2)
	} else if 3 <= a+b {
		fmt.Println(3)
	} else {
		fmt.Println(4)
	}
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

