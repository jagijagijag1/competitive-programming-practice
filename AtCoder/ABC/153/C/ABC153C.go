package main

import (
	"bufio"
	"fmt"
	"os"
	"sort"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	n, k, h := nextInt(), nextInt(), []int{}
	for i := 0; i < n; i++ {
		h = append(h, nextInt())
	}
	sort.Ints(h)

	res := 0
	for i := n - 1 - k; 0 <= i; i-- {
		res += h[i]
	}
	fmt.Println(res)
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

