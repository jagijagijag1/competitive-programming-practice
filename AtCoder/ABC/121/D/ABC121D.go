package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	A, B := nextInt(), nextInt()
	fmt.Println(xorFrom0To(B) ^ xorFrom0To(A-1))
}

func xorFrom0To(n int) int {
	c := (n + 1) / 2
	res := c % 2
	if n%2 == 0 {
		res ^= n
	}
	return res
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

