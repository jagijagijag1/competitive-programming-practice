package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	a, b, c, k := nextInt(), nextInt(), nextInt(), nextInt()
	m := max(a, max(b, c))

	res := 0
	if m == a {
		res = b + c
	} else if m == b {
		res = c + a
	} else {
		res = a + b
	}

	for i := 0; i < k; i++ {
		m *= 2
	}
	fmt.Println(res + m)
}

func max(a, b int) int {
	if a < b {
		return b
	}
	return a
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

