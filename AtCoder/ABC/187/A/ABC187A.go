package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	a, b, sa, sb := nextInt(), nextInt(), 0, 0

	for i := 0; i < 3; i++ {
		sa += a % 10
		sb += b % 10
		a, b = a/10, b/10
	}
	fmt.Println(max(sa, sb))
}

func max(a, b int) int {
	if a < b {
		return b
	}
	return a
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

