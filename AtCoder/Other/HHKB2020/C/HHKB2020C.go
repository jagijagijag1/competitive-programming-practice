package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	n, used, m := nextInt(), make([]bool, 200001), 0
	for i := 0; i < n; i++ {
		p := nextInt()
		used[p] = true
		if p == m {
			for j := m + 1; ; j++ {
				if used[j] == false {
					m = j
					break
				}
			}
		}
		fmt.Println(m)
	}
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

