package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	N, M, K := nextInt(), nextInt(), nextInt()

	for i := 0; i <= N; i++ {
		for j := 0; j <= M; j++ {
			if N*j+(M-j)*i-i*j == K {
				fmt.Println("Yes")
				return
			}
		}
	}
	fmt.Println("No")
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

