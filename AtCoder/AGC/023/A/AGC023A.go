package main

import (
	"bufio"
	"fmt"
	"os"
	"strconv"
)

func main() {
	sc.Split(bufio.ScanWords)
	N, sum, m := nextInt(), 0, map[int]int{0: 1}
	for i := 0; i < N; i++ {
		sum += nextInt()
		m[sum]++
	}

	res := 0
	for _, v := range m {
		res += v * (v - 1) / 2
	}
	fmt.Println(res)
}

var sc = bufio.NewScanner((os.Stdin))

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	l := nextLine()
	i, e := strconv.Atoi(l)
	if e != nil {
		panic(e)
	}
	return i
}

